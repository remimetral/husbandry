var _app;

var App = function() {

  $.ajaxSetup({
    headers: { 'x-csrf-token' : $('meta[name=_token]').attr('content') }
  });

  // ------------------------------------------------------------------------------------------------------------------------------------------------------------
  // MAKE LISTED FUNCTIONS A PUBLIC
  // ------------------------------------------------------------------------------------------------------------------------------------------------------------

  return {
    init : _init
  };

  // ------------------------------------------------------------------------------------------------------------------------------------------------------------
  // INIT
  // ------------------------------------------------------------------------------------------------------------------------------------------------------------

  function _init() {

    _page_works = new pageWorks();
    _page_works.init();

    _page_about = new pageAbout();
    _page_about.init();

    _page_home = new pageHome();
    _page_home.init();

    _deeplink = new Deeplink();
    _deeplink.init(); 

    //$('#reversed_arc').circleType({radius: 160, dir:-1});

  }

};

// ------------------------------------------------------------------------------------------------------------------------------------------------------------
// LOAD APP + INIT
// ------------------------------------------------------------------------------------------------------------------------------------------------------------

$(function() {
  _app = new App();
  _app.init();
});
